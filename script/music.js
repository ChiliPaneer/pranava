// Play Pause logic
function handleMus(songName) {
  let mus = document.getElementById(songName);
  mus.paused ? playMus(songName) : pauseMus(songName);
}

function playMus(songName) {
  let allTracks = document.getElementsByTagName("audio");
  Array.from(allTracks).forEach((track) => {
    track.pause();
  });

  let allSample = document.getElementsByClassName("samplePlaying");
  Array.from(allSample).forEach((sample) => {
    sample.setAttribute("class", "samplePaused");
  });

  let currentSample = document.getElementById("sample" + songName);
  currentSample.setAttribute("class", "samplePlaying");

  let mus = document.getElementById(songName);
  mus.play();
}
function pauseMus(songName) {
  let currentSample = document.getElementById("sample" + songName);
  currentSample.setAttribute("class", "samplePaused");
  let mus = document.getElementById(songName);
  mus.pause();
}

